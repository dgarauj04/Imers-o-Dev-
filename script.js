function pesquisar() {
  // Obtém a seção onde os resultados serão exibidos
  let section = document.getElementById("resultados-pesquisa");

  let campoPesquisa = document.getElementById("campo-pesquisa").value
  
  // se campoPesquisa for uma string sem nada
  if (!campoPesquisa) {
    section.innerHTML = "<p>Não foi possivel encontrar resultados. Você não digitou na barra de pesquisa</p>"
    return
  }
  
  campoPesquisa = campoPesquisa.toLowerCase()
  
  // Inicializa uma string vazia para armazenar os resultados
  let resultados = "";
  let titulo = "";
  let descricao = "";
  let tags = "";
  
  // Itera sobre os dados e constrói o HTML para cada resultado
  for (let dado of dados) {
    titulo = dado.titulo.toLowerCase()
    descricao = dado.descricao.toLowerCase()
    tags = dado.tags.toLowerCase()
    //se titulo includes campoPequisa
    if (titulo.includes(campoPesquisa) || descricao.includes(campoPesquisa) || tags.includes(campoPesquisa)) {
       // Cria uma nova div para cada resultado
    resultados += `
      <div class="item-resultado">
        <h2>
          <a href=${dado.link} target="_blank">${dado.titulo}</a>
        </h2>
        <p class="descricao-meta">${dado.descricao}</p>
       <a> <a href=${dado.link} target="_blank"> Clique aqui</a> para acessar a ferramenta</a>
      </div>
    `;
    } 
  }
  
  if (!resultados){
    resultados = "<p>Não foi possivel encontrar resultados</p>"
  }
  
  // Atribui o HTML gerado à seção de resultados
  section.innerHTML = resultados;
}

