// Certifique-se de que o código é executado após o DOM ser carregado
document.addEventListener('DOMContentLoaded', function() {
  // Selecione os elementos HTML
  const campoPesquisa = document.getElementById('campo-pesquisa');
  const resultadosPesquisa = document.getElementById('resultados-pesquisa');
  const botaoTudo = document.getElementById('tudo');
  const botaoCategorias = document.getElementById('botao-categorias');
  const categoriasDiv = document.getElementById('categorias');
  const botaoPesquisar = document.getElementById('botao-pesquisar'); // Novo botão de pesquisa
 
  // Verifique se os elementos foram carregados corretamente
  if (!campoPesquisa || !resultadosPesquisa || !botaoTudo || !botaoCategorias || !categoriasDiv || !botaoPesquisar) {
    console.error('Erro: Alguns elementos HTML não foram encontrados');
    return;
  }
  
  // Verifique se os dados e categorias estão disponíveis
  if (typeof dados === 'undefined' || typeof categorias === 'undefined') {
    console.error('Erro: Os arrays "dados" ou "categorias" não estão definidos.');
    return;
  }

  // Função para limpar o conteúdo de um elemento
  function limparDiv(div) {
    div.innerHTML = '';
  }

  // Função para mostrar todos os resultados
  function tudo() {
    // Verifica se o container de resultados já está visível
    if (resultadosPesquisa.style.display === 'block') {
      // Se estiver visível, esconde o conteúdo
      resultadosPesquisa.style.display = 'none';
      limparDiv(resultadosPesquisa);
        } else {
      // Limpe o container de resultados
    limparDiv(resultadosPesquisa); // Limpe o container de resultados
    limparDiv(categoriasDiv); // Limpe o container de categorias
    resultadosPesquisa.innerHTML = '';
    categoriasDiv.style.display = 'none';
    resultadosPesquisa.style.display = 'block';

    // Percorra os dados e adicione-os ao container de resultados
    dados.forEach((dado) => {
      const resultado = document.createElement('div');
      resultado.innerHTML = `
        <h2>${dado.titulo}</h2>
        <p>${dado.descricao}</p>
        <a href="${dado.link}" target="_blank">Acesse aqui</a>
      `;
      resultadosPesquisa.appendChild(resultado);
    });
   }
  }

  // Função para pesquisar
  function pesquisar() {
    // Obtenha o valor do campo de pesquisa
    const valorPesquisa = campoPesquisa.value.toLowerCase();
    
    if (!valorPesquisa) {
      resultadosPesquisa.innerHTML = '<p>Por favor, insira um termo para pesquisar.</p>';
      return;
    }

    // Limpe o container de resultados
    limparDiv(resultadosPesquisa); // Limpe o container de resultados
    resultadosPesquisa.innerHTML = '';
    categoriasDiv.style.display = 'none';
    resultadosPesquisa.style.display = 'block';

    // Percorra os dados e adicione-os ao container de resultados se o título ou descrição contiver o valor de pesquisa
    let resultadosFiltrados = false;
    dados.forEach((dado) => {
      if (dado.titulo.toLowerCase().includes(valorPesquisa) || dado.descricao.toLowerCase().includes(valorPesquisa) || dado.tags.toLowerCase().includes(valorPesquisa)) {
        const resultado = document.createElement('div');
        resultado.innerHTML = `
          <h2>${dado.titulo}</h2>
          <p>${dado.descricao}</p>
          <a href="${dado.link}" target="_blank">Acesse aqui</a>
        `;
        resultadosPesquisa.appendChild(resultado);
        resultadosFiltrados = true;
      }
    });

    if (!resultadosFiltrados) {
      resultadosPesquisa.innerHTML = '<p>Nenhum resultado encontrado.</p>';
    }
  }

  // Função para mostrar ou esconder categorias
  function mostrarCategorias() {
    // Verifica se o container de categorias já está visível
    if (categoriasDiv.style.display === 'block') {
      // Se estiver visível, esconde o conteúdo
      categoriasDiv.style.display = 'none';
      limparDiv(categoriasDiv);
    } else {
    limparDiv(categoriasDiv); // Limpa o container de categorias
    limparDiv(resultadosPesquisa); // Limpe o container de resultados
    resultadosPesquisa.style.display = 'none'; // Esconde resultados
    categoriasDiv.style.display = 'block'; // Mostra categorias

    // Limpa e adiciona categorias
    categorias.forEach((categoria) => {
      const button = document.createElement('button');
      button.className = 'categoria-button'; // Adiciona classe para categoria
      button.innerText = categoria.name;
      button.onclick = () => mostrarSubcategorias(categoria);
      categoriasDiv.appendChild(button);

      // Div para subcategorias
      const subcategoriasDiv = document.createElement('div');
      subcategoriasDiv.id = `subcategorias-${categoria.name}`;
      subcategoriasDiv.className = 'subcategorias-div'; // Adiciona classe para subcategorias
      subcategoriasDiv.style.display = 'none';
      subcategoriasDiv.style.marginLeft = '20px';
      categoriasDiv.appendChild(subcategoriasDiv);
    });
  }
 }
  // Função para mostrar ou esconder subcategorias
  function mostrarSubcategorias(categoria) {
    const subcategoriasDiv = document.getElementById(`subcategorias-${categoria.name}`);
    limparDiv(subcategoriasDiv);
    
    if (subcategoriasDiv.style.display === 'block') {
      subcategoriasDiv.style.display = 'none';
    } else {
      categoria.subcategorias.forEach(subcategoria => {
        const button = document.createElement('button');
        button.className = 'subcategoria-button'; // Adiciona classe para subcategoria
        button.innerText = subcategoria.name;
        button.onclick = () => mostrarInformacoes(subcategoria);
        subcategoriasDiv.appendChild(button);

        // Div para informações
        const informacoesDiv = document.createElement('div');
        informacoesDiv.id = `informacoes-${subcategoria.name}`;
        informacoesDiv.className = 'informacoes-div'; // Adiciona classe para informações
        informacoesDiv.style.display = 'none';
        informacoesDiv.style.marginLeft = '20px';
        subcategoriasDiv.appendChild(informacoesDiv);
      });
      subcategoriasDiv.style.display = 'block';
    }
  }

  // Função para mostrar ou esconder informações
  function mostrarInformacoes(subcategoria) {
    const informacoesDiv = document.getElementById(`informacoes-${subcategoria.name}`);
    limparDiv(informacoesDiv);
    
    if (informacoesDiv.style.display === 'block') {
      informacoesDiv.style.display = 'none';
    } else {
      subcategoria.informacoes.forEach(info => {
        const item = criarItemResultado(info);
        informacoesDiv.appendChild(item);
      });
      informacoesDiv.style.display = 'block';
    }
  }

  // Função para criar um item de resultado
  function criarItemResultado(dado) {
    const div = document.createElement('div');
    const titulo = document.createElement('h3');
    const descricao = document.createElement('p');
    const link = document.createElement('a');

    titulo.textContent = dado.titulo;
    descricao.textContent = dado.descricao;
    link.textContent = 'Acesse aqui';
    link.href = dado.link;
    link.target = '_blank';

    div.appendChild(titulo);
    div.appendChild(descricao);
    div.appendChild(link);
    return div;
  }

  // Adicionar listeners de eventos DENTRO do DOMContentLoaded
  botaoTudo.addEventListener('click', tudo);
  botaoCategorias.addEventListener('click', mostrarCategorias);
  botaoPesquisar.addEventListener('click', pesquisar); // Botão de clique para pesquisa
  campoPesquisa.addEventListener('keyup', function(event) {
    if (event.key === 'Enter') {
      pesquisar(); // Permitir pesquisa com Enter
    }
  });
});